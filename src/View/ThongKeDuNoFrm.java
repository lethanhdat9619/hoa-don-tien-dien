    private void PopularTable() {
        try {
            DefaultTableModel model=(DefaultTableModel) CustomerNotEnoughTable.getModel();
            list=ctr.GetHoGiaDinh();
            list.forEach((hoGiaDinh s) -> {
                model.addRow(new Object[]{s.getMaHo(),s.getTenHo(),s.getSdt(),s.getDiaChi(),s.getTongSoHoaDonThanhToan(),s.getSoTienConNo()});
            });
            
        } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(ThongKeDuNoFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
