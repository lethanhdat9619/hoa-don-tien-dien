    private void CustomerNotEnoughTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_CustomerNotEnoughTableMouseClicked
        DefaultTableModel model = (DefaultTableModel)CustomerNotEnoughTable.getModel();
        int selectRowIndex=CustomerNotEnoughTable.getSelectedRow();
        String id=model.getValueAt(selectRowIndex, 0).toString();
        hoGiaDinh ho=null;
        for (Iterator<hoGiaDinh> it = list.iterator(); it.hasNext();) {
            hoGiaDinh s = it.next();
            if(s.getMaHo().contentEquals(id)){
                ho=s;
                break;
            }
        }
        ThongKeChiTietFrm a=new ThongKeChiTietFrm(ho);
        a.setVisible(true);
    }//GEN-LAST:event_CustomerNotEnoughTableMouseClicked

